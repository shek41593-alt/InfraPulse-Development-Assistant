#!/usr/bin/env bash

###############################################################################
# InfraPulse Color Library
###############################################################################

############################
# Reset
############################

RESET="\033[0m"

############################
# Regular Colors
############################

BLACK="\033[30m"
RED="\033[31m"
GREEN="\033[32m"
YELLOW="\033[33m"
BLUE="\033[34m"
MAGENTA="\033[35m"
CYAN="\033[36m"
WHITE="\033[37m"

############################
# Bright Colors
############################

BRIGHT_BLACK="\033[90m"
BRIGHT_RED="\033[91m"
BRIGHT_GREEN="\033[92m"
BRIGHT_YELLOW="\033[93m"
BRIGHT_BLUE="\033[94m"
BRIGHT_MAGENTA="\033[95m"
BRIGHT_CYAN="\033[96m"
BRIGHT_WHITE="\033[97m"

############################
# Text Styles
############################

BOLD="\033[1m"
DIM="\033[2m"
UNDERLINE="\033[4m"

###############################################################################
# Status Helpers
###############################################################################

status_color() {

    case "$1" in

        healthy|HEALTHY|active|enabled|Connected|Running)
            printf "${BRIGHT_GREEN}%s${RESET}" "$1"
            ;;

        warning|WARNING)
            printf "${BRIGHT_YELLOW}%s${RESET}" "$1"
            ;;

        critical|CRITICAL|inactive|Stopped|Disconnected)
            printf "${BRIGHT_RED}%s${RESET}" "$1"
            ;;

        *)
            printf "%s" "$1"
            ;;
    esac
}

###############################################################################
# Section Title
###############################################################################

section_title() {

    echo

    printf "${BOLD}${BRIGHT_CYAN}"
    printf "=========================================================\n"
    printf "%s\n" "$1"
    printf "=========================================================\n"
    printf "${RESET}"
}

###############################################################################
# Key : Value
###############################################################################

print_row() {

    printf "%-24s : %s\n" "$1" "$2"
}
